// Helper functions
export function searchFoods(query: string): Food[] {
  const searchLower = query.toLowerCase();
  return foods.filter(
    (food) =>
      food.name.toLowerCase().includes(searchLower) ||
      food.nameNepali?.toLowerCase().includes(searchLower) ||
      food.nameHindi?.toLowerCase().includes(searchLower) ||
      food.tags.some((tag) => tag.toLowerCase().includes(searchLower))
  );
}

export function getFoodById(id: string): Food | undefined {
  return foods.find((food) => food.id === id);
}

export function getFoodsByCategory(category: FoodCategory): Food[] {
  return foods.filter((food) => food.category === category);
}

export function getFoodsByMealType(mealType: MealType): Food[] {
  return foods.filter((food) => food.mealType.includes(mealType));
}

export function getPopularFoods(limit: number = 10): Food[] {
  return [...foods]
    .sort((a, b) => b.popularityScore - a.popularityScore)
    .slice(0, limit);
}

export function getDiabeticFriendlyFoods(): Food[] {
  return foods.filter((food) => food.diabeticSuitability === "safe");
}

export function getFastingFoods(): Food[] {
  return foods.filter((food) => food.fastingSuitable);
}
